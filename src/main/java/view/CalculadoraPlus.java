    private boolean compararValoresF(int operador, String valor1, String valor2) {
        boolean valorBooleano = false;
        switch (operador) {
            case 0 -> // ==
                valorBooleano = control.Equals(Float.parseFloat(valor1), Float.parseFloat(valor2));
            case 1 -> // !=
                valorBooleano = control.DesEquals(Float.parseFloat(valor1), Float.parseFloat(valor2));
            case 2 -> // <
                valorBooleano = control.MinorThat(Float.parseFloat(valor1), Float.parseFloat(valor2));
            case 3 -> // <=
                valorBooleano = control.MinorOrEqualThat(Float.parseFloat(valor1), Float.parseFloat(valor2));
            case 4 -> // >
                valorBooleano = control.BiggerThat(Float.parseFloat(valor1), Float.parseFloat(valor2));
            case 5 -> // >=
                valorBooleano = control.BiggerOrEqualThat(Float.parseFloat(valor1), Float.parseFloat(valor2));
            default ->
                JOptionPane.showMessageDialog(null, "Elige una operación.");
        }
        return valorBooleano;
    }

