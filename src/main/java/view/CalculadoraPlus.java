    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Operator = ComBoxBool.getSelectedIndex();
        try {
            switch (Operator) {
                case 1 -> {
                    // && (AND condicional)
                    if (compararValoresF(OperatorNum1.getSelectedIndex(), VauleBol1.getText(), VauleBol2.getText())
                            && compararValoresF(OperatorNum2.getSelectedIndex(), VauleBol3.getText(), VauleBol4.getText())) {
                        mostrarResult(true);
                    } else {
                        mostrarResult(false);
                    }
                }

                case 2 -> {
                    // || (OR condicional)
                    if (compararValoresF(OperatorNum1.getSelectedIndex(), VauleBol1.getText(), VauleBol2.getText())
                            || compararValoresF(OperatorNum2.getSelectedIndex(), VauleBol3.getText(), VauleBol4.getText())) {
                        mostrarResult(true);
                    } else {
                        mostrarResult(false);
                    }
                }

                case 3 -> {
                    // ? : (operador ternario)
                    if ((VauleBol3.getText().isEmpty()) || (VauleBol4.getText().isEmpty())) {
                        error(2);
                    } else {
                        if (compararValoresF(OperatorNum1.getSelectedIndex(), VauleBol1.getText(), VauleBol2.getText())) {
                            jLabel1.setText(VauleBol3.getText());
                        } else {
                            jLabel1.setText(VauleBol4.getText());
                        }
                    }
                }

                default ->
                    JOptionPane.showMessageDialog(null, "Elige una operación.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Lo que escribió no son números", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Hay errores, revise sus números", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

